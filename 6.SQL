SELECT
    CASE 
        WHEN GRADE < 8 THEN 'NULL'
        ELSE NAME
    END AS NAME,
    GRADE,
    MARKS
FROM STUDENTS
JOIN GRADES
  ON MARKS BETWEEN MIN_MARK AND MAX_MARK
ORDER BY 
    GRADE DESC,
    CASE 
        WHEN GRADE >= 8 THEN NAME
        ELSE NULL
    END ASC,
    CASE 
        WHEN GRADE < 8 THEN MARKS
        ELSE NULL
    END ASC;
